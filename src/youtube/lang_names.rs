pub fn lang_name_fa(code: &str) -> String {
    let lower = code.to_ascii_lowercase();
    if let Some((_, name)) = LANG_MAP.iter().find(|(c, _)| c.eq_ignore_ascii_case(&lower)) {
        return (*name).to_string();
    }
    if let Some(dash) = lower.find('-') {
        let prefix = &lower[..dash];
        if let Some((_, name)) = LANG_MAP.iter().find(|(c, _)| c.eq_ignore_ascii_case(prefix)) {
            return (*name).to_string();
        }
    }
    code.to_string()
}

